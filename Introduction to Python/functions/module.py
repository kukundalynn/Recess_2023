def add(a, b):
    return a+b


def subtract(a, b):
    return a-b


def multiply(a, b):
    return a*b


def divide(a, b):
    return a/b


def square(a):
    return a*a


def cube(a):
    return a*a*a


def square_root(a):
    return a**(1/2)


def cube_root(a):
    return a**(1/3)
